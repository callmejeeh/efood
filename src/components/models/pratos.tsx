class Pratos {
  descricao: string
  imagem: string
  titulo: string
  id: number

  constructor(descricao: string, imagem: string, titulo: string, id: number) {
    this.id = id
    this.descricao = descricao
    this.imagem = imagem
    this.titulo = titulo
    this.id = id
  }
}

export default Pratos
